     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(ContactformRequest $request)
    {
        Mail::send('auth.emails.contactconfirm', ['email' => $request->email, 'name' => $request->name ],function ($message) use ($request) {
            $message->to($request->email, $request->name)->subject('Contact Confirmation');
        });
        return response(['response' => 'Successfully Send']);
    }

